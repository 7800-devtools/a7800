/*

Summit Coin - 8035 hardware

NEC D8035LC used as processor
needs inputs, prom decode, sound, artwork (lamps), probably some irq masking and the like

This hardware could be enhanced with a 6845 to run video AWPs, and is the original Deal 'Em
hardware. We don't have the ROMs for that though.
*/


#include "emu.h"
#include "cpu/mcs48/mcs48.h"


class sumt8035_state : public driver_device
{
public:
	sumt8035_state(const machine_config &mconfig, device_type type, const char *tag)
		: driver_device(mconfig, type, tag),
			m_maincpu(*this, "maincpu")
	{ }

protected:

	// devices
	required_device<cpu_device> m_maincpu;
};


